package loopsFor;

public class Demo {

	public static void main(String[] args) {
		
		//initialization, condition, increment or decrement
		for(int a = 1; a<=5; a++) {
			System.out.println(a);
		}
		
		for(int b = 5; b>=1; b--) {
			System.out.println(b);
		}
	

	
	for(int a = 1; a<=10; a++) {
		int num = a*3;
		if(num %2 ==0) {
			System.out.println(num);
		}
	}
	}
}


